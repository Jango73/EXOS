for (let c = 0x41; c <= 0x5A; c++) {
    const upper = String.fromCharCode(c);
    const lower = String.fromCharCode(c + 0x20);
    GLYPHS[lower] = GLYPHS[upper];
}

if (!GLYPHS["?"]) {
    throw new Error("Missing fallback '?' glyph.");
}

function buildGlyph(pattern) {
    const srcHeight = 7;
    const srcWidth = 5;
    const dstHeight = 16;
    const dstWidth = 8;
    const rows = [];

    for (let y = 0; y < dstHeight; y++) {
        const srcY = Math.floor((y * srcHeight) / dstHeight);
        let value = 0;
        for (let x = 0; x < dstWidth; x++) {
            const srcX = Math.floor((x * srcWidth) / dstWidth);
            if (pattern[srcY][srcX] === "#") {
                value |= (1 << (7 - x));
            }
        }
        rows.push(value);
    }

    if (rows.length !== dstHeight) {
        throw new Error("Glyph height mismatch.");
    }
    return rows;
}

const glyphData = [];
for (let code = 0; code < 256; code++) {
    if (code < 0x20 || code > 0x7E) {
        glyphData.push(...buildGlyph(GLYPHS[" "]));
        continue;
    }
    const ch = String.fromCharCode(code);
    const pattern = GLYPHS[ch] || GLYPHS["?"];
    glyphData.push(...buildGlyph(pattern));
}

